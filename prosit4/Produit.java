package prosit4;

class Produit {
    private String nom;
    private double prix;

    private string labelle;

    public Produit(String nom, double prix) {
        this.nom = nom;
        this.prix = prix;
        this.labelle= labelle;
    }

    public String getNom() {
        return nom;
    }

    public double getPrix() {
        return prix;
    }

    public string getlabelle() {
        return labelle;
    }
}